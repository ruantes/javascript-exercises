var valor1 = Number(prompt("Digite um número: "))
var valor2 = Number(prompt("Digite outro número: "))
var valor3 = Number(prompt("Digite mais um número: "))
var valor4 = Number(prompt("Digite outro número: "))

var media = (valor1 + valor2 + valor3 + valor4) / 4

alert(`A média desses valores é ${media}`)